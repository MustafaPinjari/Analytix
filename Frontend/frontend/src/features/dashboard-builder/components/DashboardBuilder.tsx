const GLOSSARY_TERMS: Record<string, string> = {
  'MRR': 'Monthly Recurring Revenue: The predictable total revenue generated by your SaaS subscription business per month.',
  'Churn Rate': 'The percentage of subscribers who discontinue their service subscriptions within a given time period.',
  'LTV': 'Lifetime Value: The total revenue a customer is expected to spend in your business during their relationship.',
  'CAC': 'Customer Acquisition Cost: The total cost of sales and marketing needed to acquire a single customer.',
  'ARPU': 'Average Revenue Per User: The average amount of money generated by a single user/client over a period.'
};

function GlossaryText({ text }: { text: string }) {
  const terms = Object.keys(GLOSSARY_TERMS);
  if (!text) return null;
  
  let parts: any[] = [text];
  terms.forEach(term => {
    const nextParts: any[] = [];
    parts.forEach(part => {
      if (typeof part !== 'string') {
        nextParts.push(part);
        return;
      }
      const regex = new RegExp(`\\b(${term})\\b`, 'gi');
      const split = part.split(regex);
      for (let i = 0; i < split.length; i++) {
        if (i % 2 === 1) {
          nextParts.push(
            <span 
              key={`${term}-${i}`} 
              className="underline decoration-dashed decoration-primary cursor-help relative group"
              title={GLOSSARY_TERMS[term]}
            >
              {split[i]}
              <span className="invisible group-hover:visible absolute left-1/2 -translate-x-1/2 bottom-full mb-2 w-48 bg-zinc-950 text-white text-[9px] p-2 rounded-lg shadow-xl z-50 font-normal leading-normal whitespace-normal transition-all duration-200">
                {GLOSSARY_TERMS[term]}
              </span>
            </span>
          );
        } else {
          nextParts.push(split[i]);
        }
      }
    });
    parts = nextParts;
  });
  
  return <>{parts}</>;
}

